package model.movie;

public class RankDTO {
	private String targetDt;
	private int rank;
	private String movieCd;
	private String movieNm;
	private String openDt;
	private String audiCnt;
	private String audiAcc;
	private String oldAndNew;
	private String updateRank;
	private String poster;

	public String getTargetDt() {
		return targetDt;
	}

	public void setTargetDt(String targetDt) {
		this.targetDt = targetDt;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public String getMovieCd() {
		return movieCd;
	}

	public void setMovieCd(String movieCd) {
		this.movieCd = movieCd;
	}

	public String getMovieNm() {
		return movieNm;
	}

	public void setMovieNm(String movieNm) {
		this.movieNm = movieNm;
	}

	public String getOpenDt() {
		return openDt;
	}

	public void setOpenDt(String openDt) {
		this.openDt = openDt;
	}

	public String getAudiCnt() {
		return audiCnt;
	}

	public void setAudiCnt(String audiCnt) {
		this.audiCnt = audiCnt;
	}

	public String getAudiAcc() {
		return audiAcc;
	}

	public void setAudiAcc(String audiAcc) {
		this.audiAcc = audiAcc;
	}

	public String getOldAndNew() {
		return oldAndNew;
	}

	public void setOldAndNew(String oldAndNew) {
		this.oldAndNew = oldAndNew;
	}

	public String getUpdateRank() {
		return updateRank;
	}

	public void setUpdateRank(String updateRank) {
		this.updateRank = updateRank;
	}

	public String getPoster() {
		return poster;
	}

	public void setPoster(String poster) {
		this.poster = poster;
	}

}
